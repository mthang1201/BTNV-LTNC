int binarySearch(int start, int end, int chosenNum) {
    int mid = (end + start) / 2;
    
    if (arr[mid] == chosenNum) return mid;
    
    if (arr[mid] < chosenNum) {
        mid = binarySearch(mid, end, chosenNum);
    }
    else if (arr[mid] > chosenNum) {
        mid = binarySearch(start, mid, chosenNum);
    }
    
    return mid;
}

